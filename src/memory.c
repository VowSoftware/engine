#include <vowengine/memory.h>

#include <stdlib.h>

////////////////////////////////////////////////////////////////////////////////
// Public Functions
////////////////////////////////////////////////////////////////////////////////

void* vow_memory_allocate(size_t byte_count)
{
    void* memory = malloc(byte_count);
    return memory;
}

void vow_memory_free(void* memory)
{
    free(memory);
    memory = 0;
}