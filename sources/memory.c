////////////////////////////////////////////////////////////////////////////////
// Dependencies
////////////////////////////////////////////////////////////////////////////////

#include <vowengine/memory.h>

#include <assert.h>
#include <stdlib.h>

////////////////////////////////////////////////////////////////////////////////
// Functions
////////////////////////////////////////////////////////////////////////////////

void* vow_memory_allocate(int bytes)
{
    void* memory = calloc(1, bytes);
    assert(memory);
    return memory;
}

void vow_memory_free(void* memory)
{
    free(memory);
}