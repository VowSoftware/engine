////////////////////////////////////////////////////////////////////////////////
// Dependencies
////////////////////////////////////////////////////////////////////////////////

#include <vowframework/private/log.h>

#include <vowframework/log.h>

#include <stdarg.h>
#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
// Constants
////////////////////////////////////////////////////////////////////////////////

static const char* g_level_strings[] =
{
    "INFO",
    "WARNING",
    "ERROR",
    "FATAL"
};

////////////////////////////////////////////////////////////////////////////////
// Variables
////////////////////////////////////////////////////////////////////////////////

static VowLogLevel g_level = VOW_LOG_LEVEL_INFO;

////////////////////////////////////////////////////////////////////////////////
// Functions
////////////////////////////////////////////////////////////////////////////////

void vow_log_print(VowLogLevel level, const char* format, ...)
{
    if (level < g_level) return;
    const char* level_string = g_level_strings[level];
    (void) printf("VOW %s: ", level_string);
    va_list args;
    va_start(args, format);
    (void) vprintf(format, args);
    va_end(args);
    (void) printf("\n");
}

void vow_log_level(VowLogLevel level)
{
    g_level = level;
}